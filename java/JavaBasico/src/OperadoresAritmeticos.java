
public class OperadoresAritmeticos {

	public static void main(String[] args) {
		
		//Suma
		int suma = 200 + 5;
		
		//Resta
		int resta = 200 - 50;
		
		//Multitplicación
		int mutiplicacion = 5*5;
		
		//División
		float division = 10/3f;
		
		//Modulo
		
		int modulo = 5%2;
		
		int numero = 10;
		//Post incremento
		numero++;
		//pre incremento
		++numero;
		
		//Decremento
		numero--;
		--numero;
		
		//numero = numero + 2
		numero += 2;
		
		//numero = numero - 10
		numero -= 10;
		
		//numero = numero * 6
		numero *= 6;
		
		//numero = numero / 7 no se poine la f
		numero /= 7f;
		
		System.out.println(suma);
		System.out.println(resta);
		System.out.println(mutiplicacion);
		System.out.println(division);
		System.out.println(modulo);
		
		System.out.println(numero);


	}

}
